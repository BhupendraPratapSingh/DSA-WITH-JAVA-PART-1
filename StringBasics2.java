/* toLowerCase() , toUpperCase() and concat() */
import java.io.*;
public class StringBasics2 {
    public static void main(String[] args) {
        
    
    String str = "PHYSICS WALLAH";
    System.out.println(str.toLowerCase());
    //similary we can use uppercase
    String s = "college wallah";
    System.out.println(s.toUpperCase());

    //Concatinating two String
    System.out.println(str.concat(s));



     

    
}}
